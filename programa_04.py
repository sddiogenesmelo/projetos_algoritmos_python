# Escreva um programa que leia dois números e faça as contas e imprima o resultado da soma, subtração, mutiplicação e divisão.

print("Olá seja bem vindo!!")

valor1 = float (input("\nDigite o primeiro valor: ")) # Entrada valor 1
valor2 = float (input("\nDigite o segundo valor: ")) # Entrada valor 2

soma = valor1 + valor2
print("\nO valor da soma é:",soma) # Resultado da soma

sub = valor1 - valor2
print("\nO valor da subtração é:",sub) # Resultado da subtração

mult = valor1 * valor2
print("\nO valor da multiplicação é:", mult) # Resultado da multiplicação

div = valor1 / valor2
print("\nO valor da divisão é:",div) # Resultado da divisão